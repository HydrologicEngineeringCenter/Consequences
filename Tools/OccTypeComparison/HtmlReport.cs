    private static void AppendCoverage(StringBuilder html, List<OccTypeComparisonResult> results)
    {
        html.Append("""
<section>
<h2>Coverage matrix</h2>
<p>Which occupancy types each source defines. All HEC-FIA names also exist in go-consequences; the reverse does not hold.</p>
<div class="scroll"><table class="coverage">
<thead><tr><th>Occupancy type</th><th>HEC-FIA SQLite</th><th>go-consequences</th><th>Worst depth-curve verdict</th></tr></thead>
<tbody>
""");
        foreach (OccTypeComparisonResult result in results)
        {
            string verdictCell = "<span class=\"muted\">n/a — single source</span>";
            if (result.Fia is not null && result.Go is not null && result.CurveComparisons.Count > 0)
            {
                CurveComparison worst = result.CurveComparisons.OrderByDescending(c => c.MaxAbsDiff).First();
                verdictCell = $"{Badge(worst.Verdict)} <span class=\"muted\">max {worst.MaxAbsDiff.ToString("0.0", CultureInfo.InvariantCulture)} pts</span>";
            }
            html.Append($"""
<tr><td><a href="#{Anchor(result.Name)}">{E(result.Name)}</a></td>
<td class="center">{(result.Fia is not null ? "✓" : "—")}</td>
<td class="center">{(result.Go is not null ? "✓" : "—")}</td>
<td>{verdictCell}</td></tr>
""");
        }
        html.Append("</tbody></table></div></section>\n");
    }

    private static void AppendInventory(StringBuilder html, List<InventoryRow> inventory, int fiaTotal, int goTotal)
    {
        html.Append($"""
<section>
<h2>Attribute inventory — what each source carries</h2>
<p>Counts are occupancy types carrying the item (of {fiaTotal} HEC-FIA / {goTotal} go-consequences types). Items with a zero column exist in only one source — these are the gaps the experts must decide on.</p>
<div class="scroll"><table>
<thead><tr><th>Kind</th><th>Item</th><th>HEC-FIA SQLite</th><th>go-consequences</th></tr></thead>
<tbody>
""");
        foreach (InventoryRow row in inventory)
        {
            string cls = row.FiaCount == 0 || row.GoCount == 0 ? " class=\"exclusive\"" : "";
            html.Append($"<tr{cls}><td>{E(row.Kind)}</td><td><code>{E(row.Item)}</code></td>" +
                        $"<td class=\"center\">{(row.FiaCount == 0 ? "—" : row.FiaCount.ToString())}</td>" +
                        $"<td class=\"center\">{(row.GoCount == 0 ? "—" : row.GoCount.ToString())}</td></tr>\n");
        }
        html.Append("</tbody></table></div></section>\n");
    }

    private static void AppendMethodology(StringBuilder html)
    {
        html.Append("""
<section>
<h2>Methodology</h2>
<ul>
  <li><strong>Comparable ground:</strong> structure and contents <em>depth</em>-driven damage functions. go-consequences uses its <code>depth</code> driver (falling back to <code>default</code>, which mirrors it). Vehicle/other curves, erosion, salinity and wave drivers have no counterpart in the other source and are cataloged in the inventory instead.</li>
  <li><strong>Central values:</strong> charts and diffs use each ordinate's central estimate — Deterministic value, Triangular most-likely, Normal mean, Empirical histogram mean. HEC-FIA curve ordinates are deterministic except 16 curves carrying Normal standard deviations.</li>
  <li><strong>Uncertainty bands:</strong> shaded where a source has one — Triangular min→max, Normal ±2σ, Empirical min→max.</li>
  <li><strong>Diff metric:</strong> both curves evaluated on the union of their depth grids; outside a curve's range its endpoint value extends flat. Max/mean absolute difference is in percent-damage points.</li>
  <li><strong>Verdicts:</strong> Identical &lt; 0.5 pts · Minor &lt; 5 pts · Divergent ≥ 5 pts (max abs diff).</li>
</ul>
</section>
""");
    }

    private static void AppendSharedTypes(StringBuilder html, List<OccTypeComparisonResult> shared)
    {
        html.Append("""
<section>
<h2>Shared occupancy types — depth-damage curves</h2>
<p>Sorted most-divergent first, so the discussion agenda starts at the top. Hover any curve for values; raw ordinates are in the collapsed tables.</p>
""");
        foreach (OccTypeComparisonResult result in shared.OrderByDescending(r => r.Severity).ThenBy(r => r.Name, StringComparer.Ordinal))
        {
            html.Append($"""
<article class="occtype" id="{Anchor(result.Name)}">
<h3>{E(result.Name)} <span class="desc">{E(result.Fia?.Description ?? "")}</span>
    <span class="chip">{E(result.Fia?.DamageCategory ?? "")}</span></h3>
<div class="charts">
""");
            foreach (CurveComparison comparison in result.CurveComparisons)
            {
                AppendComparisonFigure(html, result.Name, comparison);
            }
            html.Append("</div>\n");
            AppendParameterDetails(html, result);
            html.Append("</article>\n");
        }
        html.Append("</section>\n");
    }

    private static void AppendComparisonFigure(StringBuilder html, string typeName, CurveComparison comparison)
    {
        string stats = comparison.Verdict is CurveVerdict.MissingInFia or CurveVerdict.MissingInGo
            ? ""
            : $" <span class=\"muted\">max {comparison.MaxAbsDiff.ToString("0.0", CultureInfo.InvariantCulture)} · mean {comparison.MeanAbsDiff.ToString("0.0", CultureInfo.InvariantCulture)} pts</span>";

        html.Append($"""
<figure>
<figcaption>
  <span class="fig-title">{E(Cap(comparison.Component))}</span>
  {Badge(comparison.Verdict)}{stats}
</figcaption>
<div class="legend">
  <span class="key"><span class="swatch s1"></span>HEC-FIA SQLite</span>
  <span class="key"><span class="swatch s2"></span>go-consequences</span>
</div>
{Chart(comparison.Fia, comparison.Go, $"{typeName} {comparison.Component}")}
{OrdinateTable(comparison)}
</figure>
""");
    }

    private static void AppendParameterDetails(StringBuilder html, OccTypeComparisonResult result)
    {
        if (result.Fia is null || result.Go is null) return;

        html.Append($"<details><summary>Parameters &amp; source-exclusive content for {E(result.Name)}</summary><div class=\"detail-grid\">");

        html.Append("<div><h4>HEC-FIA parameters (no go-consequences counterpart)</h4><table><tbody>");
        foreach ((string key, string value) in result.Fia.Parameters.OrderBy(p => p.Key, StringComparer.Ordinal))
        {
            html.Append($"<tr><td><code>{E(key)}</code></td><td>{E(value)}</td></tr>");
        }
        html.Append("</tbody></table></div>");

        html.Append("<div><h4>go-consequences damage functions beyond depth</h4><table><tbody>");
        var extraCurves = result.Go.Curves
            .Where(c => c.Driver is not "depth" and not "default")
            .OrderBy(c => c.Component, StringComparer.Ordinal).ThenBy(c => c.Driver, StringComparer.Ordinal);
        bool any = false;
        foreach (CanonicalCurve curve in extraCurves)
        {
            any = true;
            html.Append($"<tr><td><code>{E(curve.Component)} × {E(curve.Driver)}</code></td><td>{E(curve.CurveSource ?? "")} <span class=\"muted\">({curve.Xs.Length} pts, uncertainty: {E(curve.UncertaintyKind)})</span></td></tr>");
        }
        if (!any) html.Append("<tr><td colspan=\"2\" class=\"muted\">none</td></tr>");

        var fiaExtra = result.Fia.Curves.Where(c => c.Component is "other" or "vehicle").ToList();
        if (fiaExtra.Count > 0)
        {
            html.Append("<tr><td colspan=\"2\"><h4>HEC-FIA damage functions beyond structure/contents</h4></td></tr>");
            foreach (CanonicalCurve curve in fiaExtra)
            {
                html.Append($"<tr><td><code>{E(curve.Component)} × depth</code></td><td><span class=\"muted\">{curve.Xs.Length} pts, uncertainty: {E(curve.UncertaintyKind)}</span></td></tr>");
            }
        }
        html.Append("</tbody></table></div></div></details>\n");
    }

    private static void AppendGoOnlyAppendix(StringBuilder html, List<OccTypeComparisonResult> goOnly)
    {
        if (goOnly.Count == 0) return;
        html.Append("""
<section>
<h2>Appendix — types only in go-consequences</h2>
<p>No HEC-FIA counterpart exists; shown for completeness of the decision space.</p>
""");
        foreach (OccTypeComparisonResult result in goOnly)
        {
            html.Append($"<article class=\"occtype\" id=\"{Anchor(result.Name)}\"><h3>{E(result.Name)}</h3><div class=\"charts\">");
            foreach (string component in new[] { "structure", "contents" })
            {
                CanonicalCurve? curve = Comparator.DepthCurve(result.Go!, component);
                if (curve is not null)
                {
                    html.Append($"""
<figure>
<figcaption><span class="fig-title">{E(Cap(component))}</span> <span class="muted">{E(curve.CurveSource ?? "")}</span></figcaption>
{Chart(null, curve, $"{result.Name} {component}")}
</figure>
""");
                }
                else
                {
                    // Most go-only types are NACCS coastal entries with no depth curve at all;
                    // list what they do carry so the decision space stays visible.
                    var drivers = result.Go!.Curves.Where(c => c.Component == component)
                        .Select(c => $"<li><code>{E(c.Driver)}</code> <span class=\"muted\">{E(c.CurveSource ?? "")}</span></li>");
                    html.Append($"""
<figure>
<figcaption><span class="fig-title">{E(Cap(component))}</span> <span class="muted">no depth-driven curve</span></figcaption>
<ul class="driver-list">{string.Concat(drivers)}</ul>
</figure>
""");
                }
            }
            html.Append("</div></article>\n");
        }
        html.Append("</section>\n");
    }

    // ---------------------------------------------------------------- chart (inline SVG)

    private static string Chart(CanonicalCurve? fia, CanonicalCurve? go, string chartId)
    {
        var curves = new List<(CanonicalCurve Curve, string SeriesClass, string SeriesName)>();
        if (fia is not null) curves.Add((fia, "s1", "HEC-FIA SQLite"));
        if (go is not null) curves.Add((go, "s2", "go-consequences"));
        if (curves.Count == 0) return "<div class=\"nodata\">No depth-driven curve.</div>";

        double xMin = curves.Min(c => c.Curve.Xs.Min());
        double xMax = curves.Max(c => c.Curve.Xs.Max());
        double yMax = Math.Max(100, curves.Max(c => (c.Curve.YMaxs ?? c.Curve.Ys).Max()));
        double yMin = 0;

        double PlotX(double x) => MarginLeft + (x - xMin) / (xMax - xMin) * (Width - MarginLeft - MarginRight);
        double PlotY(double y) => Height - MarginBottom - (y - yMin) / (yMax - yMin) * (Height - MarginTop - MarginBottom);

        var svg = new StringBuilder();
        svg.Append($"<svg viewBox=\"0 0 {Width} {Height}\" class=\"chart\" role=\"img\" aria-label=\"Depth-damage curves for {E(chartId)}\">");

        // Horizontal gridlines + y ticks
        foreach (double tick in Ticks(yMin, yMax, 4))
        {
            double y = PlotY(tick);
            svg.Append($"<line class=\"grid\" x1=\"{F(MarginLeft)}\" x2=\"{F(Width - MarginRight)}\" y1=\"{F(y)}\" y2=\"{F(y)}\"/>");
            svg.Append($"<text class=\"tick\" x=\"{F(MarginLeft - 6)}\" y=\"{F(y + 3)}\" text-anchor=\"end\">{F(tick)}</text>");
        }
        // X ticks + baseline
        foreach (double tick in Ticks(xMin, xMax, 6))
        {
            double x = PlotX(tick);
            svg.Append($"<text class=\"tick\" x=\"{F(x)}\" y=\"{F(Height - MarginBottom + 14)}\" text-anchor=\"middle\">{F(tick)}</text>");
        }
        svg.Append($"<line class=\"axis\" x1=\"{F(MarginLeft)}\" x2=\"{F(Width - MarginRight)}\" y1=\"{F(PlotY(0))}\" y2=\"{F(PlotY(0))}\"/>");

        // Axis titles (muted ink, never series-colored)
        svg.Append($"<text class=\"axis-title\" x=\"{F((MarginLeft + Width - MarginRight) / 2.0)}\" y=\"{F(Height - 4)}\" text-anchor=\"middle\">Depth above first floor (ft)</text>");
        svg.Append($"<text class=\"axis-title\" transform=\"translate(10,{F((MarginTop + Height - MarginBottom) / 2.0)}) rotate(-90)\" text-anchor=\"middle\">Damage (%)</text>");

        // Uncertainty bands first (washes under the lines)
        foreach ((CanonicalCurve curve, string seriesClass, _) in curves)
        {
            if (curve.YMins is null || curve.YMaxs is null) continue;
            var band = new StringBuilder("<path class=\"band ");
            band.Append(seriesClass).Append("\" d=\"M");
            for (int i = 0; i < curve.Xs.Length; i++)
                band.Append($"{F(PlotX(curve.Xs[i]))},{F(PlotY(Math.Min(curve.YMaxs[i], yMax)))} ");
            for (int i = curve.Xs.Length - 1; i >= 0; i--)
                band.Append($"{F(PlotX(curve.Xs[i]))},{F(PlotY(Math.Max(curve.YMins[i], yMin)))} ");
            band.Append("Z\"/>");
            svg.Append(band);
        }

        // Lines
        foreach ((CanonicalCurve curve, string seriesClass, _) in curves)
        {
            svg.Append($"<polyline class=\"line {seriesClass}\" points=\"");
            for (int i = 0; i < curve.Xs.Length; i++)
                svg.Append($"{F(PlotX(curve.Xs[i]))},{F(PlotY(curve.Ys[i]))} ");
            svg.Append("\"/>");
        }

        // Invisible hover targets, drawn last so they sit on top
        foreach ((CanonicalCurve curve, _, string seriesName) in curves)
        {
            for (int i = 0; i < curve.Xs.Length; i++)
            {
                string tip = $"{seriesName} · depth {F(curve.Xs[i])} ft → {F(Math.Round(curve.Ys[i], 1))}%";
                if (curve.YMins is not null && curve.YMaxs![i] > curve.YMins[i])
                    tip += $" [{F(Math.Round(curve.YMins[i], 1))}–{F(Math.Round(curve.YMaxs[i], 1))}]";
                svg.Append($"<circle class=\"hit\" cx=\"{F(PlotX(curve.Xs[i]))}\" cy=\"{F(PlotY(curve.Ys[i]))}\" r=\"9\" data-tip=\"{E(tip)}\"/>");
            }
        }

        svg.Append("</svg>");
        return svg.ToString();
    }

    private static string OrdinateTable(CurveComparison comparison)
    {
        var html = new StringBuilder("<details><summary>Raw ordinates</summary><div class=\"detail-grid\">");
        foreach ((CanonicalCurve? curve, string label) in new[] { (comparison.Fia, "HEC-FIA SQLite"), (comparison.Go, "go-consequences") })
        {
            html.Append($"<div><h4>{E(label)}</h4>");
            if (curve is null) { html.Append("<p class=\"muted\">absent</p></div>"); continue; }
            html.Append($"<p class=\"muted\">source: {E(curve.CurveSource ?? "—")} · uncertainty: {E(curve.UncertaintyKind)}</p>");
            html.Append("<table class=\"nums\"><thead><tr><th>Depth (ft)</th><th>Damage (%)</th>");
            bool hasBand = curve.YMins is not null;
            if (hasBand) html.Append("<th>Band</th>");
            html.Append("</tr></thead><tbody>");
            for (int i = 0; i < curve.Xs.Length; i++)
            {
                html.Append($"<tr><td>{F(curve.Xs[i])}</td><td>{F(Math.Round(curve.Ys[i], 2))}</td>");
                if (hasBand) html.Append($"<td>{F(Math.Round(curve.YMins![i], 2))} – {F(Math.Round(curve.YMaxs![i], 2))}</td>");
                html.Append("</tr>");
            }
            html.Append("</tbody></table></div>");
        }
        html.Append("</div></details>");
        return html.ToString();
    }

    // ---------------------------------------------------------------- helpers

    private static IEnumerable<double> Ticks(double min, double max, int approximateCount)
    {
        double rawStep = (max - min) / approximateCount;
        double magnitude = Math.Pow(10, Math.Floor(Math.Log10(rawStep)));
        double step = new[] { 1, 2, 2.5, 5, 10 }.Select(m => m * magnitude).First(s => s >= rawStep);
        for (double tick = Math.Ceiling(min / step) * step; tick <= max + step / 1000; tick += step)
            yield return Math.Round(tick, 6);
    }

    private static string Badge(CurveVerdict verdict) => verdict switch
    {
        CurveVerdict.Identical => "<span class=\"badge b-good\">✓ Identical</span>",
        CurveVerdict.Minor => "<span class=\"badge b-warn\">△ Minor</span>",
        CurveVerdict.Divergent => "<span class=\"badge b-crit\">✕ Divergent</span>",
        CurveVerdict.MissingInFia => "<span class=\"badge b-miss\">absent in HEC-FIA</span>",
        _ => "<span class=\"badge b-miss\">absent in go-consequences</span>",
    };

    private static string Anchor(string name) => "ot-" + name.Replace(' ', '-');
    private static string Cap(string s) => char.ToUpperInvariant(s[0]) + s[1..];
    private static string E(string s) => HttpUtility.HtmlEncode(s);
    private static string F(double v) => (v == 0 ? 0d : v).ToString("0.##", CultureInfo.InvariantCulture);


using System.Globalization;
using System.Text;
using System.Web;

namespace OccTypeComparison;

public record ReportProvenance(string SqlitePath, string JsonUrl, string JsonCommit, string GeneratedOn);

/// <summary>
/// Renders the comparison as a single self-contained HTML page: no external scripts,
/// styles, or fonts, so it can be emailed or opened offline on the USACE network.
/// </summary>
public static class HtmlReport
{
    // Chart geometry
    private const int Width = 460;
    private const int Height = 260;
    private const int MarginLeft = 44, MarginRight = 12, MarginTop = 12, MarginBottom = 34;

    public static string Render(
        List<OccTypeComparisonResult> results,
        List<InventoryRow> inventory,
        ReportProvenance provenance)
    {
        var shared = results.Where(r => r.Fia is not null && r.Go is not null).ToList();
        var fiaOnly = results.Where(r => r.Go is null).ToList();
        var goOnly = results.Where(r => r.Fia is null).ToList();

        var allCurveComparisons = shared.SelectMany(r => r.CurveComparisons).ToList();
        int identical = allCurveComparisons.Count(c => c.Verdict == CurveVerdict.Identical);
        int minor = allCurveComparisons.Count(c => c.Verdict == CurveVerdict.Minor);
        int divergent = allCurveComparisons.Count(c => c.Verdict == CurveVerdict.Divergent);
        int missing = allCurveComparisons.Count(c => c.Verdict is CurveVerdict.MissingInFia or CurveVerdict.MissingInGo);

        var html = new StringBuilder();
        html.Append($$"""
<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>Occupancy Type Defaults — HEC-FIA vs go-consequences</title>
<style>
{{Css()}}
</style>
</head>
<body>
<div class="viz-root">
<header>
  <h1>Occupancy Type Defaults — source comparison</h1>
  <p class="subtitle">HEC-FIA SQLite defaults vs <code>go-consequences</code> occtypes.json, prepared for the ConsequencesCore defaults decision.</p>
  <p class="provenance">
    SQLite: <code>{{E(provenance.SqlitePath)}}</code> &nbsp;·&nbsp;
    JSON: <a href="{{E(provenance.JsonUrl)}}">USACE/go-consequences structures/occtypes.json</a> @ <code>{{E(provenance.JsonCommit)}}</code> &nbsp;·&nbsp;
    Generated {{E(provenance.GeneratedOn)}}
  </p>
</header>
""");

        AppendSummary(html, shared.Count, fiaOnly.Count, goOnly.Count, identical, minor, divergent, missing);
        AppendCoverage(html, results);
        AppendInventory(html, inventory, shared.Count + fiaOnly.Count, shared.Count + goOnly.Count);
        AppendMethodology(html);
        AppendSharedTypes(html, shared);
        AppendGoOnlyAppendix(html, goOnly);

        html.Append($$"""
<footer>Generated by Tools/OccTypeComparison in ConsequencesCore. Central values only are charted; see methodology for band definitions.</footer>
<div id="tooltip" role="status" aria-hidden="true"></div>
</div>
<script>
{{TooltipScript()}}
</script>
</body>
</html>
""");
        return html.ToString();
    }


<div class="tiles">
  <div class="tile"><div class="tile-label">Depth curves compared</div><div class="tile-value">{identical + minor + divergent + missing}</div></div>
  <div class="tile"><div class="tile-label"><span class="badge b-good">✓ Identical</span> max diff &lt; 0.5 pts</div><div class="tile-value">{identical}</div></div>
  <div class="tile"><div class="tile-label"><span class="badge b-warn">△ Minor</span> max diff &lt; 5 pts</div><div class="tile-value">{minor}</div></div>
  <div class="tile"><div class="tile-label"><span class="badge b-crit">✕ Divergent</span> max diff ≥ 5 pts</div><div class="tile-value">{divergent}</div></div>
